### 1. Преобразование целого числа в шестнадцатеричное строковое представление:
def int_to_hex(num):
    hex_str = hex(num)  # Используем встроенную функцию hex
    return hex_str

# Запрос целого числа
num = int(input("Введите целое число: "))
hex_str = int_to_hex(num)
print("Шестнадцатеричное представление числа:", hex_str)